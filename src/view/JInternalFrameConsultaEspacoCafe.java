    public void setListEspacoCafe(List<Espaco> listEspacoCafe)
    {
        this.listEspacoCafe = listEspacoCafe;
        
        DefaultTableModel dtm = (DefaultTableModel) tabelaEspacoCafe.getModel();
        dtm.setRowCount(0);
        
        for (Espaco espaco : this.listEspacoCafe)
        {
            Object[] objEspaco = {espaco.getNome(), espaco.getLotacao()};
            dtm.addRow(objEspaco);
        }
    }
    
    public Espaco getEspacoSelecionado(int selected)
    {
        return listEspacoCafe.get(selected);
    }
    
    public void setEspacoSelecionado(List<Etapa> listEtapa)
    {
        DefaultTableModel dtm1 = (DefaultTableModel) tabelaEtapa1.getModel();
        dtm1.setRowCount(0);
        
        DefaultTableModel dtm2 = (DefaultTableModel) tabelaEtapa2.getModel();
        dtm2.setRowCount(0);
        
        for (Etapa etapa : listEtapa)
        {
            Object[] objPessoa = {etapa.getPessoa().getNome(), etapa.getPessoa().getSobrenome()};
            
            if (etapa.getTipoEtapa().getId() == 1)
            {
                dtm1.addRow(objPessoa);
            }
            else
            {
                dtm2.addRow(objPessoa);
            }
        }
    }

